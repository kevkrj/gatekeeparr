def _test_jellyseerr(url: str, api_key: str) -> dict:
    if not url or not api_key:
        return {'ok': False, 'error': 'URL and API key required'}
    try:
        from gatekeeper.services.jellyseerr import JellyseerrClient
        client = JellyseerrClient(url=url, api_key=api_key)
        ok = client.test_connection()
        return {'ok': ok, 'error': None if ok else 'Connection failed'}
    except Exception as e:
        return {'ok': False, 'error': str(e)}


