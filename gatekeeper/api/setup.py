def _test_tmdb(api_key: str) -> dict:
    if not api_key:
        return {'ok': False, 'error': 'API key required'}
    try:
        import requests as req
        resp = req.get(
            'https://api.themoviedb.org/3/configuration',
            params={'api_key': api_key},
            timeout=10,
        )
        ok = resp.status_code == 200
        return {'ok': ok, 'error': None if ok else f'HTTP {resp.status_code}'}
    except Exception as e:
        return {'ok': False, 'error': str(e)}


def _test_ai(provider: str, api_key: str, base_url: str, model: str) -> dict:
    if provider in ('claude', 'openai', 'grok') and not api_key:
        return {'ok': False, 'error': 'API key required for this provider'}
    if provider == 'ollama' and not base_url:
        return {'ok': False, 'error': 'Base URL required for Ollama'}

    try:
        if provider == 'claude':
            import requests as req
            resp = req.get(
                'https://api.anthropic.com/v1/models',
                headers={
                    'x-api-key': api_key,
                    'anthropic-version': '2023-06-01',
                },
                timeout=10,
            )
            ok = resp.status_code == 200
            return {'ok': ok, 'error': None if ok else f'HTTP {resp.status_code}'}

        elif provider == 'openai':
            import requests as req
            resp = req.get(
                'https://api.openai.com/v1/models',
                headers={'Authorization': f'Bearer {api_key}'},
                timeout=10,
            )
            ok = resp.status_code == 200
            return {'ok': ok, 'error': None if ok else f'HTTP {resp.status_code}'}

        elif provider == 'grok':
            import requests as req
            resp = req.get(
                'https://api.x.ai/v1/models',
                headers={'Authorization': f'Bearer {api_key}'},
                timeout=10,
            )
            ok = resp.status_code == 200
            return {'ok': ok, 'error': None if ok else f'HTTP {resp.status_code}'}

        elif provider == 'ollama':
            import requests as req
            resp = req.get(f'{base_url.rstrip("/")}/api/tags', timeout=10)
            ok = resp.status_code == 200
            return {'ok': ok, 'error': None if ok else f'HTTP {resp.status_code}'}

        return {'ok': False, 'error': f'Unknown provider: {provider}'}
    except Exception as e:
        return {'ok': False, 'error': str(e)}


def _test_webhook(url: str, name: str) -> dict:
    if not url:
        return {'ok': False, 'error': 'Webhook URL required'}
    try:
        import requests as req
        # Just validate the URL is reachable; don't actually post
        resp = req.head(url, timeout=10, allow_redirects=True)
        # Mattermost webhooks return 405 for HEAD but that means the endpoint exists
        ok = resp.status_code in (200, 201, 204, 405)
        return {'ok': ok, 'error': None if ok else f'HTTP {resp.status_code}'}
    except Exception as e:
        return {'ok': False, 'error': str(e)}


def _apply_env_vars(data: dict):
    """Apply config values to current process environment."""
    mapping = {
        'jellyseerr_url': 'JELLYSEERR_URL',
        'jellyseerr_api_key': 'JELLYSEERR_API_KEY',
        'radarr_url': 'RADARR_URL',
        'radarr_api_key': 'RADARR_API_KEY',
        'sonarr_url': 'SONARR_URL',
        'sonarr_api_key': 'SONARR_API_KEY',
        'tmdb_api_key': 'TMDB_API_KEY',
        'ai_provider': 'AI_PROVIDER',
        'ai_api_key': 'AI_API_KEY',
        'ai_base_url': 'AI_BASE_URL',
        'ai_model': 'AI_MODEL',
        'mattermost_webhook': 'MATTERMOST_WEBHOOK',
        'discord_webhook': 'DISCORD_WEBHOOK',
    }
    for key, env_var in mapping.items():
        val = data.get(key)
        if val:
            os.environ[env_var] = str(val)

    # Reset the global config so it reloads from new env vars
    from gatekeeper.config import set_config
    set_config(None)
